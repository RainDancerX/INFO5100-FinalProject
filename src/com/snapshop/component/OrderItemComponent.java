    private void cancelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBtnActionPerformed
        // Confirm cancellation
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to cancel this order?",
                "Cancel Order",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            // Update the order status in the database
            String updateStatusQuery = "UPDATE orders SET status = 'Cancelled' WHERE orderId = ?";

            try (Connection connection = DatabaseConnection.getConnection(); PreparedStatement statement = connection.prepareStatement(updateStatusQuery)) {

                statement.setInt(1, order.getOrderId());
                int rowsAffected = statement.executeUpdate();

                if (rowsAffected > 0) {
                    // Update status locally
                    order.setStatus("Cancelled");
                    status.setText("Cancelled");
                    javax.swing.JOptionPane.showMessageDialog(
                            this,
                            "Order has been successfully cancelled.",
                            "Order Cancelled",
                            javax.swing.JOptionPane.INFORMATION_MESSAGE
                    );
                } else {
                    javax.swing.JOptionPane.showMessageDialog(
                            this,
                            "Failed to cancel the order. Please try again later.",
                            "Error",
                            javax.swing.JOptionPane.ERROR_MESSAGE
                    );
                }

            } catch (SQLException e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "An error occurred while cancelling the order. Please try again later.",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_cancelBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        // TODO add your handling code here:
        // Confirm deletion
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete this order? This action cannot be undone.",
                "Delete Order",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            // SQL query to delete the order
            String deleteOrderQuery = "DELETE FROM orders WHERE orderId = ?";

            try (Connection connection = DatabaseConnection.getConnection(); PreparedStatement statement = connection.prepareStatement(deleteOrderQuery)) {

                statement.setInt(1, order.getOrderId());
                int rowsAffected = statement.executeUpdate();

                if (rowsAffected > 0) {
                    // Remove the component from the parent container
                    javax.swing.JOptionPane.showMessageDialog(
                            this,
                            "Order has been successfully deleted.",
                            "Order Deleted",
                            javax.swing.JOptionPane.INFORMATION_MESSAGE
                    );

                    // Remove this component from the UI
                    java.awt.Container parent = this.getParent();
                    if (parent != null) {
                        parent.remove(this);
                        parent.repaint();
                        parent.revalidate();
                    }
                } else {
                    javax.swing.JOptionPane.showMessageDialog(
                            this,
                            "Failed to delete the order. Please try again later.",
                            "Error",
                            javax.swing.JOptionPane.ERROR_MESSAGE
                    );
                }

            } catch (SQLException e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "An error occurred while deleting the order. Please try again later.",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_delBtnActionPerformed


