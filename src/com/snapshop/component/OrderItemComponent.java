    public void setData(Order order) {
        this.order = order;
        String dateTimeString = order.getOrderDate().toString();
        orderId.setText(String.valueOf(order.getOrderId()));
        orderDate.setText(dateTimeString.substring(0, 10));
        total.setText(String.format("$%.2f", order.getTotalAmount()));
        address.setText(order.getShippingAddress());
        status.setText(order.getStatus());
        quantity.setText(String.valueOf(order.getTotalQuantity()));

        StringBuilder desc = new StringBuilder();
        for (Order.OrderItem orderItem : order.getOrderItems()) {
            desc.append(orderItem.getItem().getItemName())
                    .append(" x").append(orderItem.getQuantity())
                    .append("\n");
        }
        orderDesc.setText(desc.toString());

        if (!order.getOrderItems().isEmpty()) {
            ImageIcon firstItemImage = order.getOrderItems().get(0).getItem().getImage();
            pic.setImage(firstItemImage);
        }
    }

