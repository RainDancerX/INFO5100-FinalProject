    private void loadCustomerData() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0); // Clear the table before loading data

        String query = "SELECT customerId, username, password, phone, address FROM customer";

        try (Connection connection = DatabaseConnection.getConnection(); PreparedStatement statement = connection.prepareStatement(query); ResultSet resultSet = statement.executeQuery()) {

            // Add each customer row to the table model
            while (resultSet.next()) {
                int Id = resultSet.getInt("customerId");
                String username = resultSet.getString("username");
                String password = resultSet.getString("password");
                String phone = resultSet.getString("phone");
                String address = resultSet.getString("address");

                model.addRow(new Object[]{Id, username, password, phone, address});
            }

        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to load customer data.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

