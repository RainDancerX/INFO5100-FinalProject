    public void addItem(ModelItem data) {
        Item item = new Item();
        item.setData(data);
        item.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent me) {
                if (SwingUtilities.isLeftMouseButton(me)) {
                    event.itemClick(item, data);
                }
            }
        });
        panelItem.add(item);
        panelItem.repaint();
        panelItem.revalidate();
    }

    public void setSelected(Component item) {
        for (Component com : panelItem.getComponents()) {
            Item i = (Item) com;
            if (i.isSelected()) {
                i.setSelected(false);
            }
        }
        ((Item) item).setSelected(true);
    }

    public void showItem(ModelItem data) {
        // Remove all components from the subPanel
        subPanel.removeAll();

        // Create and add the new SelectedItem panel
        item = new SelectedItem(addToCartListener);
        item.setData(data);
        subPanel.add(item, BorderLayout.CENTER);

        // Revalidate and repaint the subPanel to reflect changes
        subPanel.revalidate();
        subPanel.repaint();
    }

